module LeetCode
  # 344. Reverse String
  module LC344
    # Description:
    # Write a function that takes a string as input and returns the string reversed.
    #
    # Examples:
    # Input: "hello"
    # Output: "olleh"
    #
    # @param s {String}
    # @return {String}
    def reverse_string(s)
      s.reverse
    end
  end
end
